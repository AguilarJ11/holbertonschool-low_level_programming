#ifndef calc
#define calc

/**
 *suma - suma
 *@a: entry int
 *@b: entry int
 *Return: resultado a + b
 */

int suma(int a, int b)
{
	return (a + b);
}

/**
 *resta - resta
 *@a: entry int
 *@b: entry int
 *Return: resultado a - b
 */

int resta(int a, int b)
{
	return (a - b);
}

/**
 *multi - multiplica
 *@a: entry int
 *@b: entry int
 *Return: resultado a * b
 */

int multi(int a, int b)
{
	return (a * b);
}

/**
 *div - division
 *@a: entry int
 *@b: entry int
 *Return: resultado a / b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 *resto - resto de div
 *@a: entry int
 *@b: entry int
 *Return: resto de div entre a y b
 */

int resto(int a, int b)
{
	return (a % b);
}

#endif
